package org.javaprogrammingclass;

public class Java_Program_Class {

	// ODD Or Add

	public static void main(String[] args) {

		int num = 10;
		for (int i = 0; i <= num; i++) {

			if (i % 2 == 0) {

				System.out.println(i + " is Even Number");
			} else {
				System.out.println(i + " is odd Number");
			}

		}

	}

}
